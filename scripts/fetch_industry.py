def main():
    codes_input = sys.argv[1] if len(sys.argv) > 1 else ""
    watch_codes = [c.strip() for c in codes_input.replace("，", ",").split(",") if c.strip()]

    results = []
    unknown_codes = []

    for code in watch_codes:
        if code in INDUSTRY_MAP:
            name, industry = INDUSTRY_MAP[code]
            results.append({"code": code, "name": name, "industry": industry})
        else:
            unknown_codes.append(code)
            results.append({"code": code, "name": "", "industry": ""})

    # 尝试用 AKShare 补充未知的（可能通过代理可用）
    if unknown_codes:
        try:
            import akshare as ak
            for code in unknown_codes:
                try:
                    df = ak.stock_individual_info_em(symbol=code, timeout=10)
                    if df is not None and not df.empty:
                        info = {}
                        for _, row in df.iterrows():
                            item = str(row.get("item", ""))
                            value = row.get("value", "")
                            if item == "股票简称": info["name"] = str(value)
                            elif item == "行业": info["industry"] = str(value)
                        if info.get("industry"):
                            # 更新结果
                            for r in results:
                                if r["code"] == code:
                                    r["name"] = info.get("name", code)
                                    r["industry"] = info.get("industry", "")
                                    break
                except Exception:
                    pass
        except ImportError:
            pass

    print(json.dumps({"success": True, "data": results}, ensure_ascii=False))


if __name__ == "__main__":
    main()
